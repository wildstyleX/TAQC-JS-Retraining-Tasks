const secPerHour = 60 * 60;
const secPerDay = secPerHour * 24;
const secPerMonth = secPerDay * 30; // if there is 30 days in month

console.log(`Number of seconds in an hour: ${secPerHour}`);
console.log(`Number of seconds in a day: ${secPerDay}`);
console.log(`Number of seconds in a month (approximate): ${secPerMonth}`);